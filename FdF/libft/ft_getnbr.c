int	ft_getnbr(char* s)
{
	int	res;
	int	ptr;

	res = 0;
	ptr = 0;
	while (s[ptr] != '\0' && s[ptr] != '\n') {
		res = (res * 10) + (s[ptr] - '0');
		ptr++;
	}
	return res;
}